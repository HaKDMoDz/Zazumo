using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Phat.Messages
{
    public class ScriptActivatedEvent
    {
        public String ScriptName { get; private set; }
        public Object ScriptData { get; private set; }

        public ScriptActivatedEvent(String scriptName, Object scriptData)
        {
            this.ScriptName = scriptName;
            this.ScriptData = scriptData;
        }
    }
}
