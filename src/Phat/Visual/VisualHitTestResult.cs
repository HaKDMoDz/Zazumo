using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.Xna.Framework;

namespace Phat.Visual
{
    public class VisualHitTestResult
    {
        public Actor Actor { get; private set; }
        public Vector2 RelativeLocation { get; private set; }
        public Boolean IsHit { get; private set; }
        
        public VisualHitTestResult(Actor actor, Vector2 relativeLocation, Boolean isHit)
        {
            this.Actor = actor;
            this.RelativeLocation = relativeLocation;
            this.IsHit = isHit;
        }
    }
}
